function fv(val) {
  if (typeof val === 'boolean') return { booleanValue: val };
  if (typeof val === 'string')  return { stringValue: val };
  if (val && typeof val === 'object') {
    const fields = {};
    Object.entries(val).forEach(([k, v]) => { fields[k] = fv(v); });
    return { mapValue: { fields } };
  }
  return { nullValue: null };
}

function patchDoc(path, data) {
  return new Promise((res, rej) => {
    const url = BASE + '/' + path;
    const body = JSON.stringify({
      fields: Object.fromEntries(Object.entries(data).map(([k, v]) => [k, fv(v)]))
    });
    const u = new URL(url);
    const opt = {
      hostname: u.hostname, path: u.pathname, method: 'PATCH',
      headers: { 'Content-Type': 'application/json', 'Content-Length': Buffer.byteLength(body) }
    };
    const req = https.request(opt, r => {
      let d = '';
      r.on('data', c => d += c);
      r.on('end', () => r.statusCode < 300 ? res() : rej(new Error('HTTP ' + r.statusCode)));
    });
    req.on('error', rej);
    req.write(body);
    req.end();
  });
}

